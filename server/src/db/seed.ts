async function seed() {
  console.log("Seeding exercise catalog...");
  console.log(`Total exercises to seed: ${exercises.length}`);

  // Count by category
  const categoryCounts: Record<string, number> = {};
  for (const ex of exercises) {
    categoryCounts[ex.category] = (categoryCounts[ex.category] || 0) + 1;
  }
  console.log("Category counts:", categoryCounts);

  // Delete existing data and insert fresh
  await db.delete(exerciseCatalog);
  console.log("Deleted existing exercise catalog data.");

  let inserted = 0;
  for (const exercise of exercises) {
    try {
      await db.insert(exerciseCatalog).values(exercise);
      inserted++;
    } catch (err) {
      console.error(`Failed to insert ${exercise.name}:`, err);
    }
  }

  console.log(`Successfully seeded ${inserted}/${exercises.length} exercises.`);
  process.exit(0);
}

seed().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});
